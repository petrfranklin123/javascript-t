let a = 15.7, b = 25; 
console.log(`a = ${a}`);
console.log(`b = ${b}`);

a = -a;
console.log(`-a = ${a}`);
console.log(`${a} + ${b} = ${a + b}`);
console.log(`${a} - ${b} = ${a - b}`);
console.log(`${a} * ${b} = ${a * b}`);
console.log(`${a} / ${b} = ${a / b}`);


a = 10, b = 4;
console.log(`a = ${a}`);
console.log(`b = ${b}`);

//Остаток от деления 
console.log(`${a} % ${b} = ${a % b}`);
//Возведение в степень
console.log(`${a} ** ${b} = ${a ** b}`);

a++;
console.log(`a = ${a}`);

b--;
console.log(`b = ${b}`);

